const mongoose = require("mongoose");

const sub_todoSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    completed: {
      type: Boolean,
      default: false,
    },
    createdBy: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
    },
  },
  { timestamps: true }
);

const Sub_Todo = mongoose.model("Sub_Todo", sub_todoSchema);

module.exports = Sub_Todo;
